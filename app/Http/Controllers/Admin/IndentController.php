    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $search = $request->input('search','');
        if(!empty($search)) {
            $data = Indent::where('number','like','%'.$search.'%')->orderBy('id','asc')->paginate(10);
        }else {
            $data = Indent::orderBy('id','asc')->paginate(10);

        }

        return view('admin.indent.list',compact('data','search'));
    }

